import { 功能已启用, 监听功能变化 } from './功能开关.js'
import { 清空覆盖层, 请求覆盖层重绘 } from './覆盖层.js'
import { 状态 } from './状态.js'
import { 更新基地危险背景 } from './功能/基地危险.js'
import { 重置敌方基地推测 } from './功能/敌方基地推测.js'
import { 重置敌方最强兵力位置 } from './功能/敌方主力标记.js'
import { 清除敌方开塔提示 } from './功能/敌方开塔提示.js'
import { 清理敌方移动高亮 } from './功能/敌方移动高亮.js'
import {
  重置游戏数据进展,
  更新游戏数据进展图表,
} from './功能/游戏数据进展图表.js'
import { 重置我方行动监控, 更新我方行动监控UI } from './功能/我方行动监控.js'
import { 移除大回合倒计时 } from './功能/大回合倒计时.js'
import { 清除回合结束提示 } from './功能/回合结束提示.js'
import { 重置回放, 同步回放元素 } from './功能/回放系统.js'
import { 清除抢塔提示 } from './功能/抢塔提示.js'
import { 恢复战场塔信息 } from './功能/战场塔信息.js'
import { 恢复战场数据差 } from './功能/战场数据差.js'

let 已安装功能恢复 = false

export function 安装功能恢复() {
  if (已安装功能恢复) return
  已安装功能恢复 = true

  监听功能变化((变更) => {
    if (变更.类型 === '单项') {
      处理单项变化(变更.id, 变更.是否开启)
    } else if (变更.类型 === '总开关') {
      处理总开关变化(变更.是否开启)
    } else if (变更.类型 === '批量') {
      请求刷新()
    }
  })
}

function 处理总开关变化(是否开启) {
  if (是否开启) {
    请求刷新()
    return
  }

  移除大回合倒计时()
  清除回合结束提示()
  恢复战场塔信息()
  恢复战场数据差()
  清除敌方开塔提示()
  更新基地危险背景()
  清除覆盖层并刷新()
  状态.我方行动监控面板?.remove()
  状态.我方行动监控面板 = null
  状态.游戏数据进展面板?.remove()
  状态.游戏数据进展面板 = null
  同步回放元素()
}

function 处理单项变化(功能id, 是否开启) {
  if (是否开启) {
    请求刷新()
    return
  }

  if (功能id === '大回合倒计时') {
    移除大回合倒计时()
    清除回合结束提示()
  } else if (功能id === '回合结束提示') {
    清除回合结束提示()
  } else if (功能id === '战场塔信息') {
    恢复战场塔信息()
  } else if (功能id === '战场数据差') {
    恢复战场数据差()
  } else if (功能id === '敌方开塔提示') {
    清除敌方开塔提示()
  } else if (功能id === '基地危险提醒') {
    更新基地危险背景()
  } else if (功能id === '敌方基地推测') {
    重置敌方基地推测()
    清除覆盖层并刷新()
  } else if (功能id === '敌方主力标记') {
    重置敌方最强兵力位置()
    清除覆盖层并刷新()
  } else if (功能id === '敌方移动高亮') {
    状态.敌方移动高亮列表 = []
    清理敌方移动高亮()
    清除覆盖层并刷新()
  } else if (功能id === '抢塔提示') {
    清除抢塔提示()
    清除覆盖层并刷新()
  } else if (功能id === '我方行动监控') {
    重置我方行动监控()
    更新我方行动监控UI()
  } else if (功能id === '游戏数据进展图表') {
    重置游戏数据进展()
    更新游戏数据进展图表()
  } else if (功能id === '回放系统') {
    重置回放()
    同步回放元素()
  } else if (
    功能id === '塔记忆标记' ||
    功能id === '基地记忆标记' ||
    功能id === '未到达视野' ||
    功能id === '障碍物标记' ||
    功能id === '兵力分布着色' ||
    功能id === '移动队列轨迹' ||
    功能id === '选中棋子提示' ||
    功能id === '地图大小标签'
  ) {
    清除覆盖层并刷新()
  }
}

function 清除覆盖层并刷新() {
  清空覆盖层()
  请求刷新()
}

function 请求刷新() {
  if (!功能已启用('我方行动监控')) {
    状态.我方行动监控面板?.remove()
    状态.我方行动监控面板 = null
  }
  if (!功能已启用('游戏数据进展图表')) {
    状态.游戏数据进展面板?.remove()
    状态.游戏数据进展面板 = null
  }
  请求覆盖层重绘()
}
