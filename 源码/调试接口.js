// 功能目的:
// 在 window 上暴露 gio塔标记/gioTowerMarker 调试入口，方便在浏览器控制台查看脚本状态、
// 已记忆的塔和基地、兵力分布着色结果，以及手动补录测试数据。
//
// 作用范围:
// 这是开发和实战排查用的辅助接口，不会主动改变游戏逻辑。
// 只有调用手动方法时才会写入内存状态并触发重绘，便于验证 1v1 相关功能是否正常。
import { 敌方红色索引, 我方蓝色索引 } from './配置.js'
import { 状态 } from './状态.js'
import { 取得大回合倒计时 } from './工具.js'

export function 暴露调试接口(请求渲染, 清空覆盖层) {
  window.gio塔标记 = {
    状态() {
      return {
        宽度: 状态.宽度,
        高度: 状态.高度,
        当前回合: 状态.当前回合,
        大回合倒计时: 取得大回合倒计时(状态.当前回合),
        塔列表长度: 状态.塔列表?.length ?? null,
        已知塔数量: 状态.已知塔集合.size,
        中立塔兵力数量: 状态.中立塔兵力表.size,
        中立塔开塔成本数量: 状态.中立塔开塔成本表.size,
        我方开塔增长数量: 状态.我方开塔增长表.size,
        我方开塔数: 状态.我方开塔数,
        敌方开塔数: 状态.敌方开塔数,
        敌方开塔候选: 状态.敌方开塔候选,
        抢塔数: 状态.抢塔数,
        已知基地数量: 状态.已知基地集合.size,
        敌方塔数量: Array.from(状态.已知塔类型.values()).filter((类型) => {
          return 类型 === '敌方塔'
        }).length,
        已知敌方基地数量: 状态.已知敌方基地集合.size,
        兵力分布着色数量: 状态.兵力分布着色列表.length,
        兵力分布调试: 状态.兵力分布调试,
        敌方开塔调试: 状态.敌方开塔调试,
        敌方移动高亮数量: 状态.敌方移动高亮列表.length,
        敌方最强兵力位置: 状态.敌方最强兵力位置,
        敌方基地接触数量: 状态.敌方基地接触列表.length,
        敌方基地候选数量: 状态.敌方基地候选列表.length,
        敌方基地推测调试: 状态.敌方基地推测调试,
        围死区域数量: 状态.围死区域列表.length,
        围死格数量: 状态.围死区域集合.size,
        回放: {
          帧数量: 状态.回放帧列表.length,
          当前帧索引: 状态.回放当前帧索引,
          已结束: 状态.回放已结束,
          正在显示: 状态.回放正在显示,
        },
        我方索引: 状态.我方索引,
        队伍: 状态.队伍,
        颜色规则: {
          我方蓝色索引,
          敌方红色索引,
        },
        socket已挂钩: 状态.socket已挂钩,
      }
    },
    已知塔() {
      return Array.from(状态.已知塔集合).map((塔索引) => {
        return {
          索引: 塔索引,
          类型: 状态.已知塔类型.get(塔索引) ?? '中立塔',
          兵力: 状态.中立塔兵力表.get(塔索引) ?? null,
          开塔成本: 状态.中立塔开塔成本表.get(塔索引) ?? null,
          开塔增长: 状态.我方开塔增长表.get(塔索引) ?? null,
          行: 状态.宽度 ? Math.floor(塔索引 / 状态.宽度) : null,
          列: 状态.宽度 ? 塔索引 % 状态.宽度 : null,
        }
      })
    },
    已知敌方基地() {
      return Array.from(状态.已知敌方基地集合.values()).map((基地) => {
        return {
          ...基地,
          行: 状态.宽度 ? Math.floor(基地.索引 / 状态.宽度) : null,
          列: 状态.宽度 ? 基地.索引 % 状态.宽度 : null,
        }
      })
    },
    兵力分布着色() {
      return 状态.兵力分布着色列表.map((地块) => {
        return {
          索引: 地块.索引,
          兵力: 地块.兵力,
          归属: 地块.归属,
          行: 状态.宽度 ? Math.floor(地块.索引 / 状态.宽度) : null,
          列: 状态.宽度 ? 地块.索引 % 状态.宽度 : null,
        }
      })
    },
    兵力分布调试() {
      return 状态.兵力分布调试
    },
    敌方开塔调试() {
      return 状态.敌方开塔调试
    },
    性能() {
      return 状态.性能诊断
    },
    清空长任务诊断() {
      状态.性能诊断.长任务 = {
        支持:
          PerformanceObserver.supportedEntryTypes?.includes('longtask') ===
          true,
        运行中: 状态.性能诊断.长任务?.运行中 === true,
        次数: 0,
        总耗时: 0,
        平均耗时: 0,
        最近: null,
        最长: null,
        最近列表: [],
        最近关联: [],
        启动时间: Math.round(performance.now()),
      }
      return 状态.性能诊断.长任务
    },
    敌方移动高亮() {
      return 状态.敌方移动高亮列表.map((移动) => {
        return {
          ...移动,
          起点行: 状态.宽度 ? Math.floor(移动.起点 / 状态.宽度) : null,
          起点列: 状态.宽度 ? 移动.起点 % 状态.宽度 : null,
          终点行: 状态.宽度 ? Math.floor(移动.终点 / 状态.宽度) : null,
          终点列: 状态.宽度 ? 移动.终点 % 状态.宽度 : null,
        }
      })
    },
    敌方最强兵力位置() {
      const 记录 = 状态.敌方最强兵力位置
      if (!记录) return null
      return {
        ...记录,
        行: 状态.宽度 ? Math.floor(记录.索引 / 状态.宽度) : null,
        列: 状态.宽度 ? 记录.索引 % 状态.宽度 : null,
      }
    },
    敌方基地推测() {
      return {
        接触列表: 状态.敌方基地接触列表.map((接触) => {
          return {
            ...接触,
            行: 状态.宽度 ? Math.floor(接触.索引 / 状态.宽度) : null,
            列: 状态.宽度 ? 接触.索引 % 状态.宽度 : null,
          }
        }),
        候选列表: 状态.敌方基地候选列表.map((候选) => {
          return {
            ...候选,
            行: 状态.宽度 ? Math.floor(候选.索引 / 状态.宽度) : null,
            列: 状态.宽度 ? 候选.索引 % 状态.宽度 : null,
          }
        }),
        调试: 状态.敌方基地推测调试,
      }
    },
    手动加塔(塔索引) {
      if (Number.isInteger(塔索引) && 塔索引 >= 0) {
        状态.已知塔集合.add(塔索引)
        if (!状态.已知塔类型.has(塔索引)) 状态.已知塔类型.set(塔索引, '中立塔')
        请求渲染()
      }
    },
    手动设我方塔(塔索引) {
      if (Number.isInteger(塔索引) && 塔索引 >= 0) {
        状态.已知塔集合.add(塔索引)
        状态.已知塔类型.set(塔索引, '我方塔')
        请求渲染()
      }
    },
    手动设敌方塔(塔索引) {
      if (Number.isInteger(塔索引) && 塔索引 >= 0) {
        状态.已知塔集合.add(塔索引)
        状态.已知塔类型.set(塔索引, '敌方塔')
        请求渲染()
      }
    },
    手动加敌方基地(基地索引, 玩家索引) {
      if (Number.isInteger(基地索引) && 基地索引 >= 0) {
        状态.已知基地集合.add(基地索引)
        状态.已知敌方基地集合.set(基地索引, {
          索引: 基地索引,
          玩家索引: Number.isInteger(玩家索引) ? 玩家索引 : null,
          首次回合: null,
        })
        请求渲染()
      }
    },
    手动尺寸(宽度, 高度) {
      状态.宽度 = 宽度
      状态.高度 = 高度
      请求渲染()
    },
    清空() {
      状态.已知塔集合.clear()
      状态.已知塔类型.clear()
      状态.中立塔兵力表.clear()
      状态.中立塔开塔成本表.clear()
      状态.我方开塔增长表.clear()
      状态.我方开塔数 = 0
      状态.敌方开塔数 = 0
      状态.敌方开塔候选 = null
      状态.抢塔数 = 0
      状态.敌方开塔确认集合.clear()
      状态.已知基地集合.clear()
      状态.已知敌方基地集合.clear()
      状态.基地兵力表.clear()
      状态.已到达视野集合.clear()
      状态.已确认视野集合.clear()
      状态.已知障碍物集合.clear()
      状态.围死区域集合.clear()
      状态.围死区域列表 = []
      状态.围死区域已初始化 = false
      状态.原始兵力文本.clear()
      状态.敌方移动高亮列表 = []
      状态.敌方最强兵力位置 = null
      状态.敌方基地接触列表 = []
      状态.敌方基地候选列表 = []
      状态.敌方基地推测调试 = null
      状态.塔列表 = null
      清空覆盖层()
    },
    重绘: 请求渲染,
  }
  window.gioTowerMarker = window.gio塔标记
}
